export function isWebLocale(v: string | undefined | null): v is WebLocale {
  return !!v && (WEB_LOCALES as readonly string[]).includes(v);
}

export function resolveLocale(v: string | undefined | null): WebLocale {
  return isWebLocale(v) ? v : DEFAULT_LOCALE;
}

export function isRtl(locale: WebLocale): boolean {
  return RTL_LOCALES.has(locale);
}

// Tłumaczenie z fallbackiem: locale → en → pl → sam klucz.
export function t(locale: WebLocale, key: string): string {
  return T[locale]?.[key] ?? T.en[key] ?? T.pl[key] ?? key;
}
