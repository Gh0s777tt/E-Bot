// Pierwsze wejście bez zapisu: dopasuj język przeglądarki (pl-PL→pl, pt-BR→pt, zh-CN→zh…).
export function detectBrowserLocale(): PanelLocale {
  try {
    const raw = (navigator.language || '').toLowerCase();
    const base = raw.split('-')[0];
    if (isPanelLocale(base)) return base;
  } catch {
    /* SSR / brak navigator */
  }
  return DEFAULT_PANEL_LOCALE;
}

